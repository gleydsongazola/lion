    private void accept(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_accept
        // TODO add your handling code here:
        if (!validaInformacoes()) {
            return;
        }
        int ultimoRegistro = 0;
        boolean ok = false;
        if (stateEstado == StateEstado.EDITANDO) {
            ultimoRegistro = Integer.parseInt(jTextCodigo.getText());
            modelEstado.setIdEstado(Integer.parseInt(jTextCodigo.getText()));
        }
        modelEstado.setIdPais(Integer.parseInt(jTextCodigoPais.getText()));
        modelEstado.setSigla(jTextSigla.getText());
        modelEstado.setNome(jTextNome.getText());
        if (!jTextCodigoIbge.getText().trim().equals("")) {
            modelEstado.setCodIbge(Integer.parseInt(jTextCodigoIbge.getText()));
        }

        switch (stateEstado) {
            case INSERINDO:
                if (controllerEstado.insertEstado(modelEstado)) {
                    ultimoRegistro = controllerEstado.ultimoRegistro(modelEstado);
                    ok = true;
                }
                break;
            case EDITANDO:
                if (controllerEstado.editEstado(modelEstado)) {
                    ok = true;
                }
                break;
        }
        if (ok) {
            carregaDados(ultimoRegistro);
            stateEstado = StateEstado.CONSULTANDO;
            habilitaComponente(stateEstado);
            JOptionPane.showMessageDialog(this, "Operação realizada com sucesso!");
        }
    }//GEN-LAST:event_accept

