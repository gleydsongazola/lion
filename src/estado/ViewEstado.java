    private void carregaDados(int pFocusRegistro) {
        listaModelEstadoPais = controllerEstadoPais.getListaEstadoPais();
        DefaultTableModel tableEstado = (DefaultTableModel) jTableEstado.getModel();
        tableEstado.setNumRows(0);
        jTableEstado.updateUI();
        int cont = listaModelEstadoPais.size();
        if (cont > 0) {
            for (int i = 0; i < cont; i++) {
                tableEstado.addRow(new Object[]{
                    listaModelEstadoPais.get(i).getModelEstado().getIdEstado(),
                    listaModelEstadoPais.get(i).getModelPais().getNome(),
                    listaModelEstadoPais.get(i).getModelEstado().getNome(),
                    listaModelEstadoPais.get(i).getModelEstado().getSigla(),
                    listaModelEstadoPais.get(i).getModelEstado().getCodIbge(),
                    formatDateTime.format(listaModelEstadoPais.get(i).getModelEstado().getDataInclusao()),
                    formatDateTime.format(listaModelEstadoPais.get(i).getModelEstado().getDataAlteracao()),
                    listaModelEstadoPais.get(i).getModelEstado().getIdPais(),});
            }
            int focusRegistro = 0;
            if (pFocusRegistro > 0) {
                switch (stateEstado) {
                    case DELETANDO:
                        focusRegistro = pFocusRegistro;
                        break;
                    default:
                        int index = jTableEstado.getColumnModel().getColumnIndex("Código");
                        for (int linha = 0; linha < jTableEstado.getRowCount(); linha++) {
                            int valor = (int) jTableEstado.getValueAt(linha, index);
                            if (pFocusRegistro == valor) {
                                focusRegistro = linha;
                                break;
                            }
                        }
                        break;
                }
            }
            jTableEstado.setRowSelectionInterval(focusRegistro, focusRegistro);
        } else {
            habilitaComponente(ViewEstado.StateEstado.VAZIO);
        }

    }

