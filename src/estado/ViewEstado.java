    private void delete(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_delete
        // TODO add your handling code here:
        if (JOptionPane.showConfirmDialog(this, "Confirma exclusão do registro?", "", JOptionPane.YES_NO_OPTION) != JOptionPane.YES_OPTION) {
            return;
        }
        try {
            stateEstado = StateEstado.DELETANDO;
            if (jTextCodigo.getText() != "") {
                modelEstado.setIdEstado(Integer.parseInt(jTextCodigo.getText()));
            }
            modelEstado.setNome(jTextNome.getText());
            int focusRegistro = jTableEstado.getSelectedRow() - 1;
            if (controllerEstado.deleteEstado(modelEstado)) {
                carregaDados(focusRegistro);
                stateEstado = StateEstado.CONSULTANDO;
                if (jTableEstado.getRowCount() > 0) {
                    habilitaComponente(stateEstado);
                } else {
                    habilitaComponente(StateEstado.VAZIO);
                }
                JOptionPane.showMessageDialog(this, "Operação realizada com sucesso!");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_delete

